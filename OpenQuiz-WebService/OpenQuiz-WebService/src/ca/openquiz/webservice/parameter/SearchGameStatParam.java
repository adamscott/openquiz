package ca.openquiz.webservice.parameter;

import com.google.appengine.api.datastore.Key;


public class SearchGameStatParam {
	private Key statsFor;
	private Key context;
	private int pageNumber = 1; 
	private int resultsByPage = 50;
	
	public SearchGameStatParam()
	{}

	public int getPageNumber() {
		return pageNumber;
	}

	public void setPageNumber(int pageNumber) {
		this.pageNumber = pageNumber;
	}

	public int getResultsByPage() {
		return resultsByPage;
	}

	public void setResultsByPage(int resultsByPage) {
		this.resultsByPage = resultsByPage;
	}

	public Key getStatsFor() {
		return statsFor;
	}

	public void setStatsFor(Key statsFor) {
		this.statsFor = statsFor;
	}

	public Key getContext() {
		return context;
	}

	public void setContext(Key context) {
		this.context = context;
	}
}
